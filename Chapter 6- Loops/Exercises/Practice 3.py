#Write a Python program that uses a loop to find the sum of all the numbers from 1 to 100.

#Answer
n=0
#1 is an initial value and 100 is a final value.
for i in range (1,101):
    n+=i #variable i will add to variable n in each loop till variable i is equal to 100
print (n) #displays the sum of all numbers from 1 to 100.

    