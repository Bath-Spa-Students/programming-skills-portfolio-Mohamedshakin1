"""Write a python program with if statement that assigns 20 to the variable y and assigns 40 to the
variable z if the variable x is greater than 100."""

#Answer
x=int(input("Enter a number= "))
#if x is greater than 100 than it assigns value 20 to the variable y, and it assigns value 40 to the variable z
#no output, if x is not greater than 10

if x>100:
    y=20
    z=40

print ("y=",y)
print ("z=",z)